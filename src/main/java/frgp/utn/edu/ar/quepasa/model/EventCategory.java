package frgp.utn.edu.ar.quepasa.model;

/**
 * Enumeración de categorías de un evento.
 */
public enum EventCategory {
    /**
     * <b>Evento deportivo.</b>
     * <p>Representa eventos deportivos, como carreras y maratones.</p>
     */
    SPORTIVE,
    /**
     * <b>Evento educativo.</b>
     * <p>Representa eventos educativos, como graduaciones y actos escolares.</p>
     */
    EDUCATIVE,
    /**
     * <b>Reunión de consorcio.</b>
     * <p>También conocido como <i>Homeowners Association Meeting</i> o <i>Condominium Board Meeting</i>, son reuniones en las que los propietarios de unidades de un mismo edificio, condominio, o de un barrio discuten cuestiones administrativas, de mantenimiento o normas comunitarias.</p>
     */
    HOA_MEETING,
    /**
     * <b>Venta de garage.</b>
     * <p>Es un evento en el cual una persona vende artículos usados, como muebles, ropa o herramientas, desde su casa a vecinos o transeúntes. </p>
     */
    YARD_SALE,
    /**
     * <b>Feria.</b>
     * <p>Es un evento temporal durante el cual múltiples comerciantes ofrecen sus productos en un espacio público. </p>
     */
    FAIR,
    /**
     * <b>Fiesta.</b>
     */
    PARTY,
    /**
     * <b>Evento Turístico.</b>
     * <p>Representa eventos relacionados al turismo, como desfiles o sucesos naturales. </p>
     */
    TOURISTIC,
    /**
     * <b>Concierto.</b>
     * <p>Representa eventos de conciertos.</p>
     */
    CONCERT,
    /**
     * <b>Teatro.</b>
     * <p>Eventos de teatro.</p>
     */
    THEATRE,
    /**
     * <b>Ópera.</b>
     */
    OPERA,
    /**
     * <b>Eventos relacionados al cine.</b>
     * <p>Pueden ser noches de cine, cine al aire libre o reuniones relacionadas.</p>
     */
    CINEMA,
    /**
     * <b>Reuniones de emergencia. </b>
     * <p>Pueden ser reuniones en lugares seguros ante desastres naturales, como terremotos o huracanes. </p>
     */
    EMERGENCY,
    /**
     * <b>Eventos relacionados a la medicina.</b>
     * <p>Pueden ser ferias de salud, campañas de vacunación, entre otros. </p>
     */
    MEDICAL,
    /**
     * <b>Eventos para mascotas.</b>
     * <p>Pueden ser ferias de salud veterinaria, eventos de donación de mascotas o campañas de castración, entre otros.</p>
     */
    FOR_PETS
}
